num1  = 10
num2 = 20
# ArithmeticOperators

print("num1 + num2 = ", num1 + num2)
print("num1 - num2 = ", num1 - num2)
print("num1 * num2 = ", num1 * num2)
print("num1 / num2 = ", num1 / num2)

# Assignment Operator

a = 4
a +=2
a -=2

print(a)

